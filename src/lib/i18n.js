export function makeT(lang) {
  const table = STRINGS[lang] || STRINGS.en;
  const fallback = STRINGS.en;
  return function t(key, ...args) {
    const v = table[key] ?? fallback[key];
    if (typeof v === "function") return v(...args);
    return v ?? key;
  };
}
